const uint8_t *font5x7_char_columns(char c) {
    unsigned code = (unsigned char)c;
    if (code < FONT5X7_FIRST_CHAR || code > FONT5X7_LAST_CHAR) return NULL;
    return &FONT_DATA[(code - FONT5X7_FIRST_CHAR) * FONT5X7_WIDTH];
}

size_t font5x7_text_width(const char *text) {
    if (!text || !*text) return 0;
    size_t glyphs = strlen(text);
    return glyphs * (FONT5X7_WIDTH + 1) - 1;  // 5 px per char + 1 px spacing, minus trailing
}
